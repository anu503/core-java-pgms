package com.basic;

public class Switchstmt {

	public static void main(String[] args) {
	int i=2;
	switch(i){
	case 1:
		 System.out.println("the value of i :" + i);
		 break;
	case 2:
		 System.out.println("the value of i :" + i);
	case 3:
		 System.out.println("the value of i:" + i);
		 
		
	case 4:
		 System.out.println("the value of i:" + i);
		 break;
		 default:
			 System.out.println("break");



	}

	}

}
